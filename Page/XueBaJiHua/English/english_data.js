function set_data(data){
    var all_data='<!--使用Javascript填充-->';
    for(var x=0;x<data.data.grammar.length;x++){
        all_data+='<h1>'+data.title[x]+'</h1>';
        all_data+='<h2>Grammar Focus</h2>';
        all_data+='<table>';
        for(var i=0;i<data.data.grammar[x].length;i++){
            all_data+='<tr>';
            all_data+='<td>'+data.data.grammar[x][i][0]+'</td>';
            all_data+='<td>'+data.data.grammar[x][i][1]+'</td>';
            all_data+='</tr>';
        }
        all_data+='</table>';
        all_data+='<p class="grammar_focus">';
        for(var i=0;i<data.data.grammar_p[x].length;i++){
            all_data+='<b>'+data.data.grammar_p[x][i]+'</b>';
        }
        all_data+='</p>';
        all_data+='<h2>Role-play the conversation</h2>';
        if(data.data.conversation[x].more_people){
            for(var i=0;i<data.data.conversation[x].people.length;i++){
                all_data+='<p><b>'+data.data.conversation[x].people[i]+':</b>'+data.data.conversation[x].content[i]+'</p>';
            }
        }else{
            for(var i=0;i<data.data.conversation[x].content.length/2;i++){
                all_data+='<p><b>'+data.data.conversation[x].people[0]+':</b>'+data.data.conversation[x].content[i*2]+'</p>';
                all_data+='<p><b>'+data.data.conversation[x].people[1]+':</b>'+data.data.conversation[x].content[i*2+1]+'</p>';
            }
        }
        all_data+='<audio src="'+data.data.media[x]+'" controls="controls" id="mp3_'+(x+1)+'">你的浏览器太过陈旧,不支持播放音频.</audio>';
        all_data+='<input type="button" class="set_audio_loop" value="开启循环" onclick="set_audio_loop(\'mp3_'+(x+1)+'\')" id="mp3_'+(x+1)+'_btn"/>';
    }
    document.getElementById('body_div').innerHTML=all_data;
}